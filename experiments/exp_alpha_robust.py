"""exp_alpha_robust — seed-averaged α error bars and a sound-speed fit.

Refines exp_convergence. DiReBM is deterministic, so an "ensemble" is generated by jittering the
moment lattice by a random sub-cell offset per seed — independent realizations of the field
reconstruction. From the same runs we extract, per α:

  - the L2 distance of the radial density profile to the LBM baseline (mean ± std over seeds);
  - the compression-peak radius vs time (sub-bin, parabolic), whose slope over the propagating
    phase is the acoustic wave speed; compared against LBM and the lattice cs = 0.5.

Run: uv run python experiments/exp_alpha_robust.py
Writeup: docs/results/exp_convergence.md (robust section)
"""

from __future__ import annotations

from pathlib import Path

import matplotlib

matplotlib.use("Agg")
import matplotlib.pyplot as plt
import numpy as np

from direbm import bin_fields, equilibrium
from direbm.lbm import HexLBM
from direbm.reference import Moment, Simulator

TAU = 0.6
PULSE = 1.5
PULSE_R = 2.5
HALF = 10
STEPS = 8
SEEDS = 3
ALPHAS = [2.0, 3.0, 4.0, 5.0]
FIT_FROM = 5  # fit the peak-radius slope from this iteration (skip the pulse-formation transient,
# during which the peak "detaches" from the centre and jumps to the propagating ring)
CS = 0.5
OUT = Path(__file__).resolve().parents[1] / "docs" / "results" / "exp_alpha_robust.png"

_BINS = np.arange(0.0, HALF + 1.0, 1.0)
_CENTERS = 0.5 * (_BINS[:-1] + _BINS[1:])
_RMAX = HALF - 3
_WIN = _CENTERS < _RMAX
_H = _CENTERS[1] - _CENTERS[0]


def rest_moments(offset):
    f_rest = equilibrium(np.float64(1.0), np.zeros(2))
    out = []
    for i in range(-HALF, HALF + 1):
        for j in range(-HALF, HALF + 1):
            x = np.array([i + offset[0], j + offset[1]])
            f = equilibrium(np.float64(PULSE), np.zeros(2)) if np.hypot(*x) < PULSE_R else f_rest.copy()
            out.append(Moment(f=f, x=x))
    return out


def radial_profile(r, v):
    sums, _ = np.histogram(r, _BINS, weights=v)
    cnts, _ = np.histogram(r, _BINS)
    return sums / np.maximum(cnts, 1), cnts


def drbm_profile(sim):
    x, _, _ = sim.macroscopic()
    f = np.stack([m.f for m in sim.moments])
    rho, _, ext = bin_fields(x, f, -HALF, HALF, h=1.0)
    cx = np.linspace(ext[0] + 0.5, ext[1] - 0.5, rho.shape[1])
    cy = np.linspace(ext[2] + 0.5, ext[3] - 0.5, rho.shape[0])
    gx, gy = np.meshgrid(cx, cy)
    r = np.sqrt(gx * gx + gy * gy).ravel()
    v = rho.ravel()
    keep = v > 0
    return radial_profile(r[keep], v[keep])


def peak_subbin(prof, cnts):
    valid = (cnts > 0) & _WIN
    if not valid.any():
        return 0.0
    i = int(np.argmax(np.where(valid, prof, -np.inf)))
    if 0 < i < len(prof) - 1 and valid[i - 1] and valid[i + 1]:
        y0, y1, y2 = prof[i - 1], prof[i], prof[i + 1]
        denom = y0 - 2 * y1 + y2
        if denom < 0:  # concave → real peak; vertex of the parabola
            return _CENTERS[i] + 0.5 * (y0 - y2) / denom * _H
    return _CENTERS[i]


def lbm_run():
    lbm = HexLBM(ni=4 * HALF + 1, nj=4 * HALF + 1, tau=TAU, rho0=1.0)
    lbm.f[lbm.radius() < PULSE_R] = equilibrium(np.float64(PULSE), np.zeros(2))
    peaks, prof = [], None
    for _ in range(STEPS):
        lbm.step()
        rho, _ = lbm.macroscopic()
        prof, cnts = radial_profile(lbm.radius().ravel(), rho.ravel())
        peaks.append(peak_subbin(prof, cnts))
    return np.array(peaks), prof


def drbm_run(alpha, offset):
    sim = Simulator(rest_moments(offset), tau=TAU, alpha=alpha)
    peaks, prof = [], None
    for _ in range(STEPS):
        sim.step()
        prof, cnts = drbm_profile(sim)
        peaks.append(peak_subbin(prof, cnts))
    return np.array(peaks), prof


def slope(peaks):
    t = np.arange(1, STEPS + 1)
    m = t >= FIT_FROM
    return float(np.polyfit(t[m], peaks[m], 1)[0])


def main():
    rng = np.random.default_rng(0)
    lbm_peaks, lbm_prof = lbm_run()
    lbm_speed = slope(lbm_peaks)

    err_mean, err_std, peak_mean, speeds = [], [], {}, []
    print(f"{'alpha':>6} {'L2_err(mean+/-std)':>22} {'speed':>7}")
    for a in ALPHAS:
        errs, ptraj = [], []
        for _ in range(SEEDS):
            off = rng.uniform(0.0, 1.0, 2)
            peaks, prof = drbm_run(a, off)
            errs.append(float(np.sqrt(np.mean((prof[_WIN] - lbm_prof[_WIN]) ** 2))))
            ptraj.append(peaks)
        errs = np.array(errs)
        ptraj = np.array(ptraj)
        err_mean.append(errs.mean())
        err_std.append(errs.std())
        peak_mean[a] = ptraj.mean(axis=0)
        speeds.append(slope(ptraj.mean(axis=0)))
        print(f"{a:>6} {errs.mean():>10.4f} +/- {errs.std():<7.4f} {speeds[-1]:>7.3f}")
    print(f"LBM speed = {lbm_speed:.3f}   (lattice cs = {CS})")

    a = np.array(ALPHAS)
    ts = np.arange(1, STEPS + 1)
    fig, (p0, p1) = plt.subplots(1, 2, figsize=(13, 5), constrained_layout=True)
    p0.errorbar(a, err_mean, yerr=err_std, fmt="-s", color="tab:red", capsize=4)
    p0.set(xlabel="α", ylabel="profile L2 error vs LBM (interior)")
    p0.set_title(f"(1) profile error vs α ({SEEDS} seeds)")
    for ai, si in zip(a, speeds, strict=True):
        p1.plot(ts, peak_mean[ai], "-o", ms=4, label=f"DiReBM α={ai:.0f} (v={si:.2f})")
    p1.plot(ts, lbm_peaks, "-^", color="k", ms=5, label=f"LBM (v={lbm_speed:.2f})")
    p1.plot(ts, PULSE_R + CS * ts, "0.6", ls="--", lw=1, label=f"R₀+cs·t (cs={CS})")
    p1.set(xlabel="iteration", ylabel="compression-peak radius", title="(2) wave speed (sub-bin peak)")
    p1.legend(fontsize=8)
    fig.suptitle("DiReBM α study — seed-averaged error + sound-speed fit")
    OUT.parent.mkdir(parents=True, exist_ok=True)
    fig.savefig(OUT, dpi=120)
    print(f"\nsaved {OUT}")


if __name__ == "__main__":
    main()
